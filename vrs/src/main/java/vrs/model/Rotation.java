package vrs.model;

public class Rotation {

	private Double rx;
	private Double ry;
	private Double rz;
	private Double rw;

	public Rotation() {

	}

	public Double getRx() {
		return rx;
	}

	public void setRx(Double rx) {
		this.rx = rx;
	}

	public Double getRy() {
		return ry;
	}

	public void setRy(Double ry) {
		this.ry = ry;
	}

	public Double getRz() {
		return rz;
	}

	public void setRz(Double rz) {
		this.rz = rz;
	}

	public Double getRw() {
		return rw;
	}

	public void setRw(Double rw) {
		this.rw = rw;
	}

}
