package vrs.model;

public class Position {

	private Double px;
	private Double py;
	private Double pz;

	public Position() {

	}

	public Double getPx() {
		return px;
	}

	public void setPx(Double px) {
		this.px = px;
	}

	public Double getPy() {
		return py;
	}

	public void setPy(Double py) {
		this.py = py;
	}

	public Double getPz() {
		return pz;
	}

	public void setPz(Double pz) {
		this.pz = pz;
	}

}
